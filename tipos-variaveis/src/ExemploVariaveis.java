public class ExemploVariaveis {
    public static void main(String[] args) {
        // Tipos primitivos
        int idade = 25; // Número inteiro
        double altura = 1.75; // Número decimal
        char inicial = 'J'; // Caractere único
        boolean ativo = true; // Valor lógico verdadeiro ou falso

        // Tipo de referência (String)
        String nome = "João Silva"; // Cadeia de caracteres

        // Exibição dos valores
        System.out.println("Nome: " + nome);
        System.out.println("Idade: " + idade);
        System.out.println("Altura: " + altura);
        System.out.println("Inicial do nome: " + inicial);
        System.out.println("Está ativo? " + ativo);
    }
}